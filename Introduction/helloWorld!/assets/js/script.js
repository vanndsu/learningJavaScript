// ==============================
// Exibindo mensagens no navegador e no console
// ==============================

// alert() é uma função nativa do JavaScript usada para exibir uma janela de alerta no navegador.
// É muito útil para mostrar mensagens rápidas ao usuário, mas interrompe a navegação até que a pessoa clique em "OK".
alert('Olá Mundo!'); // Mostra um popup com a mensagem "Olá Mundo!"

// console.log() é usado para exibir mensagens no console do navegador (acessado com F12 > aba "Console").
// Muito usado por desenvolvedores para debugar (verificar valores, erros, etc.) sem interromper a execução da página.
console.log("Olá Mundo 3.0"); // Exibe "Olá Mundo 3.0" no console
