// ==============================
// Exemplo: Função simples e sua chamada correta
// ==============================

// Criamos uma função chamada `printSomething`
// A palavra-chave `function` define que estamos criando uma função
// O nome `printSomething` é o identificador que usaremos para chamá-la
// Parênteses () — mesmo vazios — indicam que a função pode receber parâmetros (neste caso, nenhum)
function printSomething() {
  // Tudo que estiver dentro das chaves { } será executado quando a função for chamada
  // Aqui usamos console.log() para exibir a mensagem "Algo" no console
  console.log("Algo");
}

// Agora chamamos a função pelo nome exato que foi declarado acima
// ⚠️ É importante escrever exatamente igual: mesmo nome, mesmas letras e sem erros
printSomething(); // Isso executa o código dentro da função
