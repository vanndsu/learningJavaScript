// ==============================
// 1) Criar uma variável com o nome do carro e exibir
// ==============================

// Criamos uma variável chamada `car` para armazenar o nome de um carro (string).
let car = "Ferrari"; // Tipo: string

// Exibimos no console uma frase incluindo o valor da variável `car`.
// Usamos vírgula no console.log para separar os valores e evitar concatenação.
console.log("Meu carro é uma:", car); // Saída: Meu carro é uma: Ferrari

// ==============================
// 2) Criar uma variável com o preço do bolo e exibir
// ==============================

// Aqui, criamos a variável `cakePrice` e atribuímos o valor 19.99.
// Esse valor é do tipo number (número com casas decimais — ponto, não vírgula).
let cakePrice = 19.99; // Tipo: number

// Exibimos o valor no console com uma mensagem descritiva.
// Lembre-se: usamos ponto (.) para separar decimais, e não vírgula (,) como em português.
console.log("O preço do bolo de chocolate é: R$", cakePrice); 
// Saída: O preço do bolo de chocolate é: R$ 19.99

